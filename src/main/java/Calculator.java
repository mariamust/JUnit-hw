public class Calculator {

    public int sum(int a, int b){
        int result = a + b;
        System.out.println(result);
        return result;
    }

    public int sub(int a, int b){
        int result = a - b;
        System.out.println(result);
        return result;
    }

    public double div(double a, double b){
        double result = a / b;
        System.out.println(result);
        return result;
    }

}
